                    En India y Sri Lanka crece una planta que es parte de la medicina tradicional ayurveda y  que hace cientos de años es muy utilizada por sus propiedades antibióticas y astringentes: la Andrographis paniculata.Ahora, un extracto de esta planta, junto a un ácido graso omega y otro extracto vegetal,  serán parte de una triada natural con la que científicos chilenos esperan retrasar y combatir el alzheimer, una enfermedad degenerativa que ya se ha convertido en la cuarta causa de muerte en el mundo.Nibaldo Inestrosa,  Premio Nacional de Ciencias Naturales (2008) y director del Centro de Envejecimiento y Regeneración (CARE Chile UC), es quien lidera esta investigación.
