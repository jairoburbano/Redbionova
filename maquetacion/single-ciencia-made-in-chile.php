                <p>
                    Los laboratorios y aulas de la Universidad Católica, han albergado prácticamente toda la carrera profesional de esta destacada científica. Si bien sus estudios de pregrado y sus inicios en la vida laboral fueron en la Universidad de Chile, las circunstancias sociales que se vivían en el país, la llevaron a trasladarse hacia Alameda 340 en 1976.
                    “Mis 39 años en la UC han sido muy buenos, he trabajado, me he dedicado a investigar y he hecho lo que he querido”, señala con una sonrisa de lado a lado. “A mí me ha tocado vivir el crecimiento de la UC, el cual ha sido empujado por mucha gente y, probablemente, se vio favorecido por la situación política”, agrega.
                </p>
                <p>
                    Y es que allí ha formado su vida. Ha educado a cientos de alumnos y pudo ganarse un espacio en medio de un entorno machista para desarrollar la ciencia tal cual como siempre la soñó.  Porque, dicho sea de paso, eso de pararse de igual a igual frente a los hombres es algo que su madre le inculcó desde la cuna y no tuvo problema para ponerlo en práctica en todo momento. De hecho, su progenitora fue una de las cientos de mujeres que marchó por las calles abogando por el voto femenino.
                </p>
                <p>
                    A fines de la década de los ’70, cuando tuvo la posibilidad de estudiar en el extranjero, la Universidad le guardó el cupo. Eso sí, como en aquellos años las cosas no eran tan estructuradas como son hoy en día, lo primero que hizo estando afuera fue el postdoctorado, algo muy extraño porque se fue sin tener aún un doctorado. “En ese tiempo era todo muy raro”, comenta.
                    Transcurridos dos años en la Universidad de Yale, el profesor Dr. Robert H. Roth con el que trabajó allí la convenció de que hiciera el doctorado. Esto era todo un desafío porque en esa universidad no tenían becas para extranjeros y, además, en la UC le habían dado autorización por solo dos años. El problema del permiso lo solucionó el mismo Profesor Roth, quien convenció a las autoridades de Santiago de extender el plazo en cuatro años. El problema de no contar con beca lo solucionó postulando a la Universidad de Saint Louis, Missouri, la cual sí tenía financiamiento para extranjeros.
                </p>
                <p>
                    Aceptada y con beca, trabajó con la Dra. Margery Beinfeld en el neuropéptido llamado colecistoquinina, que tiene directa relación con el control de lo que ocurre con la dopamina. Este  neurotransmisor tenía especial interés para ella porque su tesis de pregrado había sido sobre los efectos del alcohol en la síntesis de dopamina.
                </p>
                <p>
                    Los cuatros años pasaron y en 1985 debía estar de regreso en Chile. “Yo defendí la tesis de doctorado un día y al día siguiente tomé el avión para venirme porque el 1 de Agosto de 1985 tenía que estar trabajando en la universidad, y ese fue el día en que llegué.  Me vine directo al laboratorio, no pasé a la casa. Una vez acá, empecé a postular a proyectos”, rememora Katia Gysling.
                </p>
                <h3>La pasión científica</h3>
                <p>
                    Según recuerda, desde pequeña tuvo ese interés por la indagación.  Sin embargo, fue un hecho puntual el que despertó su interés por la ciencia. “Tuve una profesora en 4º básico que me marcó, la profesora Gloria Villanueva, quien nos enseñó el aparato digestivo. Nos decía que tratáramos de imaginarnos qué pasaba cuando uno se comía un pan con mantequilla y nos mencionaba las enzimas como algo normal. Nos hizo hacer experimentos y siempre relacionaba lo que nos estaba enseñando con la vida real. Yo creo que eso me marcó en lo que hago y en cómo enseño”.</p>
                <p>
                    Luego, estando en la enseñanza media “tuve otra profesora, Graciela Ferrada, quien nos enseñó que el ATP era la fuente de energía y yo le pregunté que por qué no comíamos ATP. Ella me dijo que para entender eso tenía que estudiar Bioquímica. Ahí yo supe que existía la carrera. A los días pregunté de qué se trataba y postulé esa carrera en la Universidad de Chile”.
                    Fue en el quinto año del pregrado cuando descubrió la farmacología y en ese momento supo que no se podría despegar más de ahí. “Mi primer trabajo científico publicado fue sobre efectos del alcohol en la síntesis de dopamina. Luego de eso, yo dejé la investigación en adicción, las drogas, porque es un tema muy competitivo. Sin embargo, hace unos años nos atrevimos a iniciar un Núcleo Milenio en el cual investigamos las adicciones porque, a pesar de que es competitivo, con el nivel actual de la ciencia en Chile era posible hacerlo. Trabajamos en mecanismos celulares y moleculares que tienen que ver con el proceso adictivo en el cerebro”, asevera.
                    El Milenio ya terminó. Ahora va de coinvestigadora en un proyecto donde si se adjudica, junto a su equipo, buscarán trabajar en el cerebro adolescente porque “hay mucho que hacer ahí ya que este cerebro es más sensible a los efectos de las drogas de abuso y a las patologías psiquiátricas”.</p>
                <p>
                    Aun cuando la investigadora ama lo que hace, ya piensa en la proximidad de su jubilación. “Es tan rápido el avance del conocimiento que es imposible que los mismos que estamos acá seamos capaces de incorporar todo lo nuevo. El nuevo conocimiento debe venir desde los más jóvenes”.
                </p>
                <h3>¿Qué le diría a los jóvenes científicos?</h3>
                <p>
                    “Les diría que se atrevan, que no se limiten para hacerse preguntas, que exploren e indaguen más allá de su área específica. Esto permite  a veces ver otras posibles alternativas de explicación a los fenómenos en estudio. Que tengan una actitud rigurosa y crítica frente a su propio trabajo y al revisar la nueva información que se publica”, finaliza.
                </p>
                <p>
                    Por Catalina Valencia Antillanca
                </p>
